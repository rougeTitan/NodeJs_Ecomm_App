// Import Mongoose for MongoDB object modeling
const mongoose = require('mongoose');

// Get Schema constructor from mongoose
const Schema = mongoose.Schema;

// Define the Product schema structure
const productSchema = new Schema({
  title: {
    type: String,
    required: true // Product title is mandatory
  },
  price: {
    type: Number,
    required: true // Product price is mandatory
  },
  description: {
    type: String,
    required: true // Product description is mandatory
  },
  imageUrl: {
    type: String,
    required: true // Product image URL is mandatory
  },
  userId: {
    type: Schema.Types.ObjectId, // Reference to User model
    ref: 'User', // Specifies which model to reference
    required: true // Product must have an associated user (owner)
  }
});

// Export the Product model based on the schema
module.exports = mongoose.model('Product', productSchema);

